package com.jichuangsi.school.user.model;

public class RoleUrlUseWayModel {
    private String id;
    private String name;
    private String url;
    private String usewayid;
    private String usewayname;
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUsewayid() {
        return usewayid;
    }

    public void setUsewayid(String usewayid) {
        this.usewayid = usewayid;
    }

    public String getUsewayname() {
        return usewayname;
    }

    public void setUsewayname(String usewayname) {
        this.usewayname = usewayname;
    }
}
